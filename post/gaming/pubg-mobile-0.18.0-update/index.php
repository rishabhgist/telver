                        <p>Tencent Games has announced that it will release the PUBG Mobile 0.18.0 update on May 7. A new report from Pocket-Lint suggests that that will also be the date when the company will reveal a newly revamped map. According to the report, the company in a number of its tweets made from the PUBG Mobile official handle has added Morse Code in the corners of the accompanying images. Morse code is a form of communication using dots and dashes.</p>
                        <p>The morse code when decoded provides individual words, ‘UPDATE’, ‘NEWMAPS’, ‘WARNING’, ‘MIRAMAR’ and ‘5-7’. This is in no particular order, but the 5-7 seems to be the date, which is May 7. Miramar is a map in the classic mode of the game, update refers to the 0.18.0 update releasing on May 7, and lastly, the NEWMAPS phrase hints at a new map.</p>
                        <img class="mb-30" src="b.jpg" alt="">
                        <p>According to reports, the beta version of the 0.18.0 update brings a new racing ramp, a new Golden Mirado vehicle and vending machines that will dispense painkillers and medkits. It will also be adding a new area called Water City. And the graphics will be improved significantly, in a similar fashion to the Erangel 2.0 map update.</p>
                        <p>Another hint pointing towards the release of the new map was spotted inside of the update’s release date announcement made by the company on Twitter. The announcement was accompanied by a video which showcased a vending machine and started with a car rumbling sound. The car is expected to be the rumoured Golden Mirado. Both of these items are expected to be a part of Miramar 2.0 map.
                        </p><p>Apart from the Miramar 2.0 map, the update will reportedly bring the addition of two new game modes: Safety Scramble Mode and Jungle Adventure Guide Mode. Both of these will be made available inside of the EvoGround menu.Mirroring its PC counterpart, the company will also be adding a second blue zone that will keep randomly showing up inside of the safe zone circle. And just like the outer blue zone circle, it will also deal damage to players health.</p>
                        
