                        <p>The ESO says that the hidden black hole in HR 6819 is one of the very first stellar-mass black holes found that do not interact violently with their immediate environment and, therefore, appear truly black.</p>
                        <p>new black hole has been discovered, and it is almost as if it is next door. In fact, this is situated just 1000 light years away, and is in the system called HR 6819 in the Constellation Telescopium. This has been discovered by astronomers at the European Southern Observatory (ESO). This discover comes as the team was observing the HR 6819 system as part of a study of double star systems. They were astonished to find that there was a previously undiscovered third body in the HR 6819 system, the black hole. In the system, one star is orbiting the black hole while the other orbits this pair.</p>
                        
                        <img class="mb-30" src="../uploads/bl-hole.jpg" alt="">
                        <p>“The observations needed to determine the period of 40 days had to be spread over several months. This was only possible thanks to ESO’s pioneering service-observing scheme under which observations are made by ESO staff on behalf of the scientists needing them,” says Dietrich Baade, Emeritus Astronomer at ESO and the co-author of the study. Usually, black holes are discovered by the way they are surrounded by gas and dust, and as they shred this material, high powered signals are emitted which are detected by telescopes. Therefore, this particularly black hole and how it has been discovered, is quite unique.</p>
                        <p>The ESO says that the hidden black hole in HR 6819 is one of the very first stellar-mass black holes found that do not interact violently with their immediate environment and, therefore, appear truly black. Its presence could only be spotted and the calculations of the mass could only be done by studying the orbit of the star in the inner pair. “An invisible object with a mass at least 4 times that of the Sun can only be a black hole,” says ESO scientist Thomas Rivinius, who led the study.
                        </p>
