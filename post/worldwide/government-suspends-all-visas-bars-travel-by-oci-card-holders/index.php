                        <p>All existing visas granted to foreigners -- except those belonging to diplomatic, official, UN international organisations, employment and project categories -- shall remain suspended till prohibition on international air travel of passengers from and to India is lifted by the government, the order said. In the other order, the Home Ministry said regular visa, e-visa or stay stipulation of foreign nationals whose visas have expired post-midnight February 1 or would expire during the ban on international air travel from India would be extended on 'GRATIS' basis just on submission of an online application.
                        </p>
                        <p>Such extensions would be for a period up to 30 days from the date of lifting of prohibition on international air travel without any overstay penalty, it said.
