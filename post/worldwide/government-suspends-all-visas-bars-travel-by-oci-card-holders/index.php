                        <p><span style="font-weight: bold;">NEW DELHI:</span> The government Tuesday suspended all existing visas granted to foreign nationals, barring a few categories, till international air travel to and from India remains shut due to the coronavirus pandemic, an order said.In a separate order, the Union Home Ministry also said it has extended on 'gratis' basis the visas of foreigners stranded in India due the lockdown. The extension will be for a period of 30 days beyond opening of international travel in the country.</p><p>
                        The ministry also said it has kept in abeyance multiple-entry life-long visas given to Overseas Citizens of India (OCI) card holders till international travel remains suspended.However, the OCI card holders who are already in India can stay in the country for "any length of time", it said.
                        </p>
