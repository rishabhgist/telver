                        <p>The OnePlus Z has long been said to be the phone for OnePlus what the 2020 iPhone SE is to Apple -- an extremely affordable phone by the brand’s standards. We have been hearing about this OnePlus Z since late last year and it has so far been a fantasy for OnePlus fans. However, it seems that OnePlus has finally got everything right with this phone and it is ready to come up with the OnePlus Z soon. How soon, you wonder? Well, a July launch for the OnePlus Z has now been tipped.</p>
                        <p>Popular tipster Max J., who has been quite accurate predicting OnePlus launch dates, has come up with the launch date for the OnePlus Z. There’s still no accurate date but Max says in his latest tweet that the OnePlus Z is coming in July. That’s almost three months after the launch of the OnePlus 8 series.</p>
                        <img class="mb-30" src="../uploads/one-plusz.jpg" alt="">
                        <p>The OnePlus Z has long been in rumours, previously as the OnePlus 8 Lite. The Z is said to be OnePlus’ idea of a flagship killer in 2020, despite the company promoting its mainstream models to the flagship phone category. It is said that OnePlus is likely to continue from where the original OnePlus X left in 2016. However, we feel that the OnePlus Z could take over the sub-Rs 30,000 segment, a space which OnePlus used to dominate back in the days.</p>
                        <p>Leaks have already suggested that OnePlus will ditch the Snapdragon 865 in favour of a MediaTek Dimensity 1000 chip, which is expected to offer the same flagship-grade performance with 5G connectivity but at a very affordable price.
                        </p>
                        
