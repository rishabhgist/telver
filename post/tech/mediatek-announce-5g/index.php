                         <p>5G connectivity is becoming a common feature on 2020’s premium and mid-range smartphones. With the bulk of 5G phones feature Qualcomm and Kirin (In Huawei and Honor’s case) chipset, it is easy to forget MediaTek also features a couple of 5G chips.</p>
                        <p>The MediaTek Dimensity 1000 and 1000L is based on 7nm fabrication process, designed for the premium mid-range smartphone segment, while the Dimensity 800 is geared towards value-oriented 5G mid-rangers. Now, the company is gearing up to launch a new 5G chipset for budget smartphones.</p>
                        <img class="mb-30" src="../uploads/mediatek-5g.jpg" alt="">
                        <p>The company has confirmed that it will announce a new 5G chipset in China on May 7. The Taiwan-based company posted a teaser on Weibo hinting at a “5G All-inclusive” product, which is likely to be a new system-on-chip that supports 5G. While it is still uncertain whether the new chipset will power budget or flagship smartphones, we believe it will arrive as part of the company’s Dimensity lineup.</p>
                        <p>MediaTek announced the Dimensity 1000 5G chipset in December last year, while the Dimensity 800 5G arrived in January 2020. While the Dimensity 1000L has not been announced by the company, the chipset arrived on the Oppo Reno 3 5G.
                        </p>
                        <p>While MediaTek’s 5G chipsets deliver performance greater or comparable to the Snapdragon 765 5G series, they are yet to stand toe-to-toe against Qualcomm’s best, in the form of the Snapdragon 865. This leads us to believe that the upcoming MediaTek chipset could be revealed as the Dimensity 2000, the most powerful chipset in the Dimensity series.</p>
                        
                        
