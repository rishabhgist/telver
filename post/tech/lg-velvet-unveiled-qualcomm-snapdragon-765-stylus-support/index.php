                        <p>Rumors about LG planning to shelve its flagship G-series in favor of a Snapdragon 700-series powered mid-range devices first materialized early last month when the company shared sketches of its new design approach. Soon thereafter, the company published a teaser video for the LG Velvet that showed off the phone in its entirety. The teaser also confirmed that the device would feature Qualcomm’s Snapdragon 765 chipset.</p>
                        <p> Towards the end of last month, the company confirmed some key specifications and, just a few days ago, we also got to see some hands-on images of the device. As per the launch schedule teased earlier, the company has now officially unveiled the LG Velvet in South Korea.</p>
                        <img class="mb-30" src="../uploads/lg-velvet.jpg" alt="">
                        <p>As confirmed in previous leaks, the LG Velvet packs in a massive 6.8-inch POLED display with a waterdrop-style notch for the selfie camera. The display has an aspect ratio of 20.5:9, with a resolution of 2460×1080 pixels. While the display specifications seem quite standard, when compared to other mid-range devices in the market today, what sets the LG Velvet apart is that it features a special digitizer that enables Wacom stylus support with up to 4096 pressure levels. To help users make the most out of the stylus support, the device comes with the Nebo handwriting app preinstalled which can automatically convert handwriting into digital notes.</p>
                        <p>Powering the device is a mid-range Qualcomm Snapdragon 765 chip, with a max boost clock of 2.2GHz and an integrated 5G modem. The Snapdragon 765 is accompanied by a respectable 8GB of RAM and 128GB of UFS 2.1 storage, which can be expanded further using the microSD card slot. In the camera department, the LG Velvet features a triple camera setup on the back which consists of a 48MP primary sensor with OIS, an 8MP ultra wide-angle camera, and a 5MP depth sensor. Over on the front, the device has a single 16MP selfie shooter.
                        </p>
                        <h5>Pricing & Availability</h5>
                        <p>The LG Velvet is currently available in South Korea in four color variants — Aurora White, Aurora Gray, Aurora Green, and Illusion Sunset — all of which feature LG’s new soft-touch finish. The device has been priced at the equivalent of just under €700 and is available on all major network operators in the country. As of now, LG hasn’t unveiled plans for an international launch but we expect to learn more about that in the following weeks.</p>
                        
